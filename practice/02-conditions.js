// single condition - (if)
let hasMoney = true;
if (hasMoney) {
  console.log('purchase phone');
}

// double condition - (if-else)
let toss = 'head';
if (toss === 'head') {
  console.log('win');
} else {
  console.log('loss');
}

// more than two condition - (if-else if-else)
let number = 7;
if (number > 7) {
  console.log('greater than 7');
} else if (number < 7) {
  console.log('less than 7');
} else {
  console.log('it\'s 7');
}